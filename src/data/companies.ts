// Get unique companies (remove duplicates)
export const UNIQUE_COMPANIES = [...new Set(ALL_COMPANIES)];

// Get random companies for a problem (1-6 companies)
export const getRandomCompanies = (min = 1, max = 6): string[] => {
  const count = Math.floor(Math.random() * (max - min + 1)) + min;
  const shuffled = [...UNIQUE_COMPANIES].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

// Get companies by category with weights (more likely to see major companies)
export const getWeightedRandomCompanies = (count = 4): string[] => {
  const weights = [
    { companies: FAANG, weight: 30 },
    { companies: USMajorTech.slice(0, 15), weight: 25 },
    { companies: FinanceGlobal.slice(0, 10), weight: 15 },
    { companies: IndianStartups.slice(0, 30), weight: 20 },
    { companies: IndianTechGiants.slice(0, 8), weight: 10 },
  ];
  
  const result: string[] = [];
  const totalWeight = weights.reduce((sum, w) => sum + w.weight, 0);
  
  while (result.length < count) {
    let rand = Math.random() * totalWeight;
    for (const { companies, weight } of weights) {
      rand -= weight;
      if (rand <= 0) {
        const company = companies[Math.floor(Math.random() * companies.length)];
        if (!result.includes(company)) {
          result.push(company);
        }
        break;
      }
    }
  }
  
  return result;
};
